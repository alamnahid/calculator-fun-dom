const result = document.getElementById('result');

document.getElementById('seven').addEventListener('click', function(){
    const sevenField = document.getElementById('seven');
    const sevenString = sevenField.innerText;
    const seven = parseFloat(sevenString);

    const p = document.createElement('p');
    p.innerText = seven;
    p.classList.add('bg-[#E2FCFF]');
    p.classList.add('text-5xl');
    p.classList.add('text-right');
    p.classList.add('mt-4');
    p.classList.add('font-semibold');
    p.classList.add('mr-4');
    result.appendChild(p)
})
